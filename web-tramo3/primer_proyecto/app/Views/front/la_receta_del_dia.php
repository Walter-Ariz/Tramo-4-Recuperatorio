           <h1>Carne al horno con papas</h1>
          </div>
          <div class="col-12">
          <h3>Ingredientes</h3>
          </div>
    </div>
    <div class="ingredientes"> 
        <p> 750 gramos de carne de ternera
       <br> 3 papas
       <br> 1 cebolla
       <br> ½ pimiento
       <br> 1 diente de ajo
       <br> 2 cucharadas soperas de aceite
       <br> 1 copa de vino
       <br> sal, pimienta, ají molido, perejil, romero y tomillo a gusto</p>
    </div>
       <h3>Preparacion:</h3>
       <div class="preparacion">
       <p> 1-Lo primero que debes hacer es encender el horno para precalentarlo a temperatura media de 180 °C. 
        Mientras se calienta, limpia el exceso de grasa exterior de la carne.<br><br>
        2-Mezcla en un recipiente amplio las 2 cucharadas de aceite con los condimentos. Pela y pica el ajo y agrega a la mezcla.<br><br>
        3-Coloca la carne argentina dentro del recipiente y úntala bien con la preparación.<br><br>
        4-Posteriormente, pela y corta la cebolla en pluma y el pimiento en tiras.<br><br>
        5-A continuación, lava y pela las papas y córtalas en cubos, del tamaño más o menos similar para que la cocción sea pareja.<br><br>
        6-Pincela con aceite una asadera para horno y coloca en un lado la cebolla y el pimiento, y en otro las papas.<br><br>
        7-Pon a calentar una sartén sobre el fuego y cuando tome temperatura, sella la carne dorándola por ambos lados. No olvides incluir la marinada. Luego, añade el vino para levantar el sabor que queda adherido a la sartén.<br><br>
        8-Cuando se evapore el alcohol, apaga el fuego y coloca la carne dorada en la asadera sobre las verduras, baña con el líquido que obtuvimos en la sartén. Lleva al horno a temperatura media de 180 °C por alrededor de 30 o 40 minutos (Dependerá del punto de cocción que prefieras). A mitad de tiempo, gira las papas y la carne y, si hace falta, agrega caldo o agua.<br><br>
        9-Cuando la carne al horno y las papas estén cocidas y doradas, ya estará listo para comer. </p>
        </div>
